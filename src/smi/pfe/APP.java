    private void tabMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabMouseClicked
        
        int lig;
        String dat,temp,src,dest;
        panAll.setVisible(true);
        try {

            lig=tab.getSelectedRow();
            dat=tab.getValueAt(lig,0).toString();
            temp=tab.getValueAt(lig,1).toString();
            src=tab.getValueAt(lig,3).toString();
            dest=tab.getValueAt(lig,4).toString();

            Statement st = getConn().createStatement();
            ResultSet rs = st.executeQuery("SELECT `Date`, `Temps`, `Protocole`, `Taille`, `Ip_src`, `Ip_dest`, `Port_src`, `Port_dest`, `Num_seq`, `Ack`,`Type`\n" +
                "from application a, segment s, paquet p, machine m, reseau r \n" +
                "where a.ID=s.ApplicationID and s.PaquetID=p.ID and p.MachineID=m.ID and m.ReseauID=r.ID \n" +
                "and date like '"+dat+"' and temps like '"+temp+"' and `Ip_src` like '"+src+"' and `Ip_dest` like '"+dest+"' \n"+
                "ORDER by p.ID");

            while(rs.next()){
                //paquet
                date.setText(" Date : "+rs.getString(1));temps.setText(" Temps : "+rs.getString(2));protocol.setText(" Protocole : "+rs.getString(3));
                ipsrc.setText(" Taille : "+rs.getString(4)+" Oct");taille.setText(" IP_src : "+rs.getString(5));ipdest.setText(" IP_dest : "+rs.getString(6));
                //segment
                psrc.setText(" Port_src : "+rs.getInt(7));pdest.setText(" Port_dest : "+rs.getInt(8));seq.setText(" Num séq : "+rs.getInt(9));ack.setText(" ACK : "+rs.getInt(10));
                //application
                type.setText(" Type : "+rs.getString(11));
            }

        } catch (Exception e) {

        }
    }//GEN-LAST:event_tabMouseClicked

